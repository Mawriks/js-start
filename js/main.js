/**
 * Функция сложения.
 * @param {Number} first первое число
 * @param {Number} second второе число
 * @returns {Number}
 */
function add(first, second){
    return first + second;
}

/**
 * Функция вычитания.
 * @param {Number} first первое число
 * @param {Number} second второе число
 * @returns {Number}
 */
 function subtract(first, second){
    return first - second;
}

/**
 * Функция умножения.
 * @param {Number} first первое число
 * @param {Number} second второе число
 * @returns {Number}
 */
 function multiply(first, second){
    return first * second;
}

/**
 * Функция деления.
 * @param {Number} first первое число
 * @param {Number} second второе число
 * @returns {Number}
 */
 function divide(first, second){
    return first / second;
}

