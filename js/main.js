/**
 * Функция математических операций.
 * @param {Number} arg1 первое число
 * @param {Number} arg2 второе число
 * @param {String} operation операция с числами
 * @returns {Number}
 */
 function startFifth() {
    console.log('Сложение: 5 + 10 = ' + mathOperation(5,10,'+'));
    console.log('Вычитание: 100 - 76 = ' + mathOperation(100,76,'-'));
    console.log('Умножение: 72 * 28 = ' + mathOperation(72,28,'*'));
    console.log('Деление: 1024 / 8 = ' + mathOperation(1024,8,'/'));
    alert('Все готово. Откройте консоль!')
 }


 function mathOperation(arg1, arg2, operation){
     switch (operation){
        case '+':
             return add(arg1,arg2);
        case '-':
            return subtract(arg1,arg2);
        case '*':
            return multiply(arg1,arg2);
        case '/':
            return divide(arg1,arg2);
     }
 }






